/*- 
 
 Conventions for action and ability descriptions
 Field that may have them:
 'a' - card with action
 'r' - card with role
 'e' - card with permanent activatable ability
 'trig' - card with triggered abilities, array 
 
 xxx/yyy - OR action, player must chooise
 xxx>yyy - Pay>Get, player must pay to get something or to do something
 xxx. - ending dot means card triggert turn based effect (should probably remove now with setaside zonning)
 xxx.yyy - dot in the middle action is split in two part, first part must be sent to server to resolve first (i.e. draw card)
 xxx!    - confirmation required before submitting (i.e. when card removed permanently)
 xxx+    - action can be repeated any amount of times 
 event:yyy - triggered ability
 
 Trigger event format
 
 l-X leading role X
 d-X dissenting role X
 f-X following role X
 *-X playing role X
 'enter' - card enters play
 'settle' - settle a planet
 'attack' - attack a planet
 'peace' - peace treaty, shortcut for d-W:i
 
 */



foreach ($this->token_types as $id => &$info){
    $rowtype = $info ['type'];
    if (isset($info ['trig'])) {
        $trig = $info ['trig'];
        if ($info && !is_array($trig)) {
            if ($trig == 'peace') {
                $trig = "d-W:i";
            }
            $info ['trig'] = [ $trig ]; // re-wire
        }
    }
    if (isset($info ['rslots'])) {
        $info ['type'] .= " has_rslots rslots_".strlen($info ['rslots']);
    }
    if (isset($info ['v'])) {
        $info ['type'] .= " vp_".$info ['v'];
    }
    if (startsWith($rowtype,'planet')) { // NOI18N
        $info ['c'] = $info ['c'] ?? -1;
        $type = $info ['t'];
        $resources = $info ['slots'] ?? '';
        $info ['type'] .= " card_planet";
        if ($resources) {
            $info ['type'] .= " has_slots slots_$resources";
        }
        $info ['type'] .= " planet_${type}";
        if (array_key_exists('a', $info)) {
            //actionable
            $info ['type'] .= " actionable";
        }
        $icons = $info ['i'] ?? ''; // icons
        if ($icons) {
            for ($j = 0; $j < strlen($icons); $j++) {
                $c = $icons [$j];
                $info ['type'] .= " has_icon_$c";
            }
        }
    } else if (startsWith($rowtype, 'tech')) {
        // add translated planet type
        $req = $info['p'];
        $ptype = $req [0];
        if (isset($info['t'])) {
            $ptype=$info['t'];
        } else {
            $info['t']=$ptype;
        }
        $pcost = strlen($info ['p']);
     
        if (isset($info ['side'])) {
            $side = $info ['side'];
            if ($side != 0) {
                $info ['type'] .= " permanent";
                if ( !isset($info ['flip'])) {       // add flip
                    $x = strrpos($id, "_");
                    $num = substr($id, $x + 1) + 0;
                    if ($side == 1)
                        $num++;
                    else
                        $num--;
                    //card_tech_E_23
                    $flip_id = getPartsPrefix($id, -1) . "_" . $num;
                    $info ['flip'] = $flip_id;
                }
            }
        }
     
        // add icons for html
        $cost = $info ['b']; // research cost

        $icons = $info ['i'] ?? ''; // icons
 
        $info ['a'] = $info ['a'] ?? ''; // action text
        if ($icons) {
            for ($j = 0; $j < strlen($icons); $j++) {
                $c = $icons [$j];
                $info ['type'] .= " has_icon_$c";
                if ($c == 'w' || $c == 'f' || $c == 's' || $c == 'i'  || $c == 'n') {
                    $info ['type'] .= " as_resource as_resource_$c";
                    $info ['asr'] = $c;
                } else if ($c == 'F' || $c == 'D' || $c == 'B') {
                    $info ['type'] .= " as_fighter as_fighter_$c";
                    $info ['asf'] = $c;
                }
            }
        }
        $resources = $info ['slots'] ?? '';
        if ($resources) {
            $info ['type'] .= " has_slots slots_$resources";
        }
        if (isset($info ['bm'])) {
            $mcost = $info ['bm'] [0];
        }
        $mcost = $info ['bm'] ?? '';
        $info ['type'] .= " ptype_${ptype} pcost_${pcost} cost_${cost} req_${req}";
        if ($mcost)
            $info ['type'] .= " mcost_${mcost}";
        if (array_key_exists('e', $info)) {
            $info ['type'] .= " activatable";
        }
        if ($info ['a']) {
            //actionable
            $info ['type'] .= " actionable";
        }
    }
}

